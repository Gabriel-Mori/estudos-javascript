let nota = 8;

switch (nota) {
  case 5:
    console.log("nota muito ruim");
    break;
  case 10:
    console.log("nota mais ou menos");
    break;
  case 12:
    console.log("esta melhorando");
    break;
  case 15:
    console.log("nota muito boa");
    break;
  case 20:
    console.log("nota maxima");
    break;

  default:
    console.log("nota invalida");
    break;
}
