const present = new Date();
console.log("getFullYear:", present.getFullYear()); // somente o ano
console.log("getMonth:", present.getMonth() + 1); // somente o mes
console.log("getDate:", present.getDate()); // dia do mes
console.log("getDay:", present.getDay()); // dia da semana
console.log("getHours:", present.getHours()); //horas do momento salvo
console.log("getMinutes:", present.getMinutes()); // minuto
console.log("getSeconds:", present.getSeconds()); // secundos
